package Basics;

public class more {
    public static void main(String[] args) {
        //-->double/double = double
        double x = 5;
        double y = 2;
        System.out.println(x/y);

        //-->int/int = int
        double a =5/2;
        System.out.println(a);;

        System.out.println(5.0/2.0);
        System.out.println(5/2);
        System.out.println(5.0/2);
        System.out.println(5/2.0);
    }
}
